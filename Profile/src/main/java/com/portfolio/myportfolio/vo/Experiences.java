package com.portfolio.myportfolio.vo;

public class Experiences {
	int person_no;
	String exp_place;
	String exp_date;
	String exp_content;
	String exp_no;
	
	public int getPerson_no() {
		return person_no;
	}
	public void setPerson_no(int person_no) {
		this.person_no = person_no;
	}
	public String getExp_place() {
		return exp_place;
	}
	public void setExp_place(String exp_place) {
		this.exp_place = exp_place;
	}
	public String getExp_date() {
		return exp_date;
	}
	public void setExp_date(String exp_date) {
		this.exp_date = exp_date;
	}
	public String getExp_content() {
		return exp_content;
	}
	public void setExp_content(String exp_content) {
		this.exp_content = exp_content;
	}
	public String getExp_no() {
		return exp_no;
	}
	public void setExp_no(String exp_no) {
		this.exp_no = exp_no;
	}
	
}
