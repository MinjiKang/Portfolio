package com.portfolio.myportfolio.vo;

public class PersonalInfo {
	int person_no;
	String my_name;
	String my_phone;
	String my_address;
	String my_email;
	
	public int getPerson_no() {
		return person_no;
	}
	public void setPerson_no(int person_no) {
		this.person_no = person_no;
	}
	public String getMy_name() {
		return my_name;
	}
	public void setMy_name(String my_name) {
		this.my_name = my_name;
	}
	public String getMy_phone() {
		return my_phone;
	}
	public void setMy_phone(String my_phone) {
		this.my_phone = my_phone;
	}
	public String getMy_address() {
		return my_address;
	}
	public void setMy_address(String my_address) {
		this.my_address = my_address;
	}
	public String getMy_email() {
		return my_email;
	}
	public void setMy_email(String my_email) {
		this.my_email = my_email;
	}
	


}
