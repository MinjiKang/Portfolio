package com.portfolio.myportfolio.vo;

public class Skills {
	
	int person_no;
	int skill_no;
	String skill_language;
	String skill_tool;
	String skill_software;
	
	public int getPerson_no() {
		return person_no;
	}
	public void setPerson_no(int person_no) {
		this.person_no = person_no;
	}
	public int getSkill_no() {
		return skill_no;
	}
	public void setSkill_no(int skill_no) {
		this.skill_no = skill_no;
	}
	public String getSkill_language() {
		return skill_language;
	}
	public void setSkill_language(String skill_language) {
		this.skill_language = skill_language;
	}
	public String getSkill_tool() {
		return skill_tool;
	}
	public void setSkill_tool(String skill_tool) {
		this.skill_tool = skill_tool;
	}
	public String getSkill_software() {
		return skill_software;
	}
	public void setSkill_software(String skill_software) {
		this.skill_software = skill_software;
	}
	
}
