package com.portfolio.myportfolio.vo;

public class Education {
	int edu_no;
	int person_no;
	String edu_school;
	String edu_city;
	String edu_major;
	String edu_date;
	
	public int getEdu_no() {
		return edu_no;
	}
	public void setEdu_no(int edu_no) {
		this.edu_no = edu_no;
	}
	public int getPerson_no() {
		return person_no;
	}
	public void setPerson_no(int person_no) {
		this.person_no = person_no;
	}
	public String getEdu_school() {
		return edu_school;
	}
	public void setEdu_school(String edu_school) {
		this.edu_school = edu_school;
	}
	public String getEdu_city() {
		return edu_city;
	}
	public void setEdu_city(String edu_city) {
		this.edu_city = edu_city;
	}
	public String getEdu_major() {
		return edu_major;
	}
	public void setEdu_major(String edu_major) {
		this.edu_major = edu_major;
	}
	public String getEdu_date() {
		return edu_date;
	}
	public void setEdu_date(String edu_date) {
		this.edu_date = edu_date;
	}
	
}
