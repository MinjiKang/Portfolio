package com.portfolio.myportfolio.vo;

public class Objective {
	int obj_no;
	int person_no;
	String obj_content;
	
	public int getObj_no() {
		return obj_no;
	}
	public void setObj_no(int obj_no) {
		this.obj_no = obj_no;
	}
	public int getPerson_no() {
		return person_no;
	}
	public void setPerson_no(int person_no) {
		this.person_no = person_no;
	}
	public String getObj_content() {
		return obj_content;
	}
	public void setObj_content(String obj_content) {
		this.obj_content = obj_content;
	}
	

}
